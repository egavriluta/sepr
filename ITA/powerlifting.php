        <p class="TextTitle">Events</p>
        
        <p>In a Competition, there are three events: bench press, squat, and deadlift. Some variations of this are found at some meets such "push-pull only" meets where lifters only compete in the bench press and deadlift, with the bench press coming first and the deadlift after. Single lift meets are often held, sometimes alongside a normal 3-lift event. This is most common in the bench press.</p>
<p>At a meet the events will follow in order: squat, then bench press, and the deadlift will be the final lift of the meet. If the federation also has an event for strict curls this will normally occur before the squat event.

        </p>
        
