        <p class="TextTitle">Training</p> 
        <p>Powerlifters practice weight training to improve performance in the three competitive lifts&#8212;the squat, bench press and deadlift. Weight training routines used in powerlifting are extremely varied. For example, some methods call for the use of many variations on the contest lifts, while others call for a more limited selection of exercises and an emphasis on mastering the contest lifts through repetition. While many powerlifting routines invoke principles of sports science, such as specific adaptation to imposed demand (SAID principle).there is some controversy around the scientific foundations of particular training methods, as exemplified by the debate over the merits of "speed work," or training to attain maximum acceleration of submaximal weights.</p>
<p>In addition to weight training, powerlifters may pursue other forms of training to improve their performance. For example, aerobic exercise may be used to improve endurance during drawn-out competitions and support recovery from weight training sessions.

