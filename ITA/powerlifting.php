        <p>A competition takes place as follows:<br/><br/>Each competitor is allowed three to four attempts on each lift depending on their standing and the organization they are lifting in (usually smallest w-class to heaviest). The lifter's best valid attempt on each lift counts toward his competition total. If two or more lifters achieve the same total, the lighter lifter ranks above the heavier lifter.</p>
        <p>Competitors are judged against other lifters of the same gender, weight class, and age. This helps to ensure that the accomplishments of lifters like Lamar Gant, who has deadlifted 5 times his bodyweight, are recognized alongside those of Benedikt Magnusson, the current All-time deadlift world record holder.</p>
