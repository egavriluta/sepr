       <p>
        <b>Powerlifting</b> is a strength sport that consists of three attempts at maximal weight on three lifts: squat, bench press, and deadlift. As in the sport of Olympic weightlifting, it involves lifting weights in three attempts. Powerlifting evolved from a sport known as odd lifts, which followed the same three-attempt format but used a wider variety of events, akin to strongman competition. Eventually odd lifts became standardized to the current three.</p>
<p>In competition, lifts may be performed equipped or un-equipped (typically referred to as raw lifting or classic in the IPF specifically). Equipment in this context refers to a supportive bench shirt or squat/deadlift suit or briefs. In some federations, knee wraps are permitted in the equipped but not un-equipped division; in others, they may be used in both equipped and un-equipped lifting. Weight belts, knee sleeves, wrist wraps and special footwear may also be used, but are not considered when distinguishing equipped from un-equipped lifting. </p>
<p>Competitions take place across the world but mostly in the United States, Canada, United Kingdom, Iceland, Egypt, Sweden, Finland, Russia and Ukraine. Powerlifting has been a Paralympic sport (bench press only) since 1984 and, under the IPF, is also a World Games sport. Local, national and international competitions have also been sanctioned by other federations operating independently of the IPF.
