            <p>There's light at the end of tunnel &mdash; spring is just a few short weeks away. Not only is it hopefully the beginning of warmer weather, but it also marks the beginning of cutting season. It's time to shed those pounds and get that lean, ripped physique that you've been working so hard to achieve.</p>

<p>Diet, the worst four-letter word in the gym that applies to the most extremes: gaining mass or losing weight. There are no cutting corners in the lifting world, only cutting calories. Eating clean can get you far, but drastically cutting calories brings an expense. To get ripped, the cutting phase involves several weeks of a caloric deficit followed with a low-carb diet. You'll only see the lean results for so long &mdash; a plateau is inevitable. That's why it's important to understand what changes occur within your body with drastic cutting and how a cheat meal can actually be beneficial.</p>
            <p>To count calories or hormones, that's the question. It's a multifaceted answer because calories influence hormones and hormones affect calories. When you're significantly cutting back on calories, your body will respond by slowing down it's metabolic rate &mdash; it's unavoidable. From there, you'll get hungrier and crave that greasy slice of pizza with a side of cheese fries. Fast forward to next winter: all that weight you lost, is now back on you. Don't be that guy in the gym with the yo-yo diet. Approaching fat loss with hunger hormones will not only decrease hunger but also control cravings while boosting your energy levels. And to bring in the facts, a 2011 study in the New England Journal of Medicine indicated that a significant calorie restriction could cause damage to leptin, ghrelin, and other hormone levels &mdash;inciting a rush of hunger and a plummet in metabolism.</p>
             <a class="fancybox" rel="group" href="images/curb-your-junk-food-cravings.jpg" > <img src="images/curb-your-junk-food-cravings.jpg" alt="workout" /></a>
