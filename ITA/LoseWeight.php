        <h2>YOUR HORMONES</h2>
          
            <p>Three hundred calories a day, 1,500 calories a week &mdash; I guarantee you that you're cravings will kick in during week three and your metabolic rate will start to slow down. Enter: cheat meal, a meal that can offset that because its function is to be a metabolic re-activator &mdash; refueling ghrelin and leptin levels. They are your two leading hunger hormones, which can assist you in shedding some serious weight. Obesity Reviews published an analysis of a group of studies on the role of leptin and ghrelin in the regulation of food intake and body weight. It concluded that leptin is involved in long-term regulation energy balance and that ghrelin is a fast-acting hormone that shows meal-related changes.</p>

<p>Your stomach is rumbling and making weird noises &mdash; yes you are hungry and that's the work of ghrelin right there. This hunger hormone is made and released by the stomach and stimulates your appetite. But how is it controlled? The answer is simple: food regulates ghrelin levels. Levels of the hormone are high before eating and when you're seriously dropping calories. When you get hit with the hunger pangs, that's when you know it's time to eat. Also, you may have read somewhere that high ghrelin levels stimulate the release of human growth hormone (HGH) from the pituitary gland. The fountain of youth hormone helps to burn fat and build muscle &mdash; it sounds like a win-win during cutting season. But what you don't know is that high ghrelin levels promote the storage of fat in the abdominal area-getting a shredded six-pack will be hard. That's why a cheat meal is a necessity; you'll reduce your constant hunger and cravings while preventing a "beer belly."</p>  
           
            <a class="fancybox" rel="group" href="images/Torso 1_0.jpg" > <img src="images/Torso 1_0.jpg" alt="workout" /></a>
            <p>Known as the gatekeeper for fat metabolism, leptin is a hormone that's secreted by fat cells and it plays a vital role in regulating appetite, food intake, and body weight. When you have a normal, balanced diet, leptin is capable of suppressing the appetite because you're consuming just enough calories and have an appropriate amount of body fat. But when you put your body through a significant caloric deficit, there will be a reduction in leptin secretion and leptin circulating in the blood in the bound form. Circulating bound leptin acts on the hypothalamus &mdash; that's how you know when you're full. But when you're leaning out, there will be a decrease in bound leptin, preventing your brain from knowing if you're full or not. A cheat meal will help control you're cravings by surpessing your appetite at the appropriate time. </p>
            
            <p class="lastP"></p>
            
          <h2>THE CHEAT MEAL</h2>
            
            <p>Instead of being at the mercy of your will power, know when to have a cheat meal. It only takes 72 hours for changes to occur with your hunger hormones &mdash; low energy and a slower metabolism. You should be consuming a cheat meal at least once every two weeks, and the cheat meal should be higher in calories and carbs. As mentioned before, a cheat meal will refuel your hunger hormones, which will also boost your metabolism. The increase in metabolism will last for a few days post-cheat meal. And to make a cheat meal really work, be smart with what you eat. Just because it's a cheat meal, doesn't mean you should overindulge; instead of going for the sweets, opt for high-protein pasta or chicken cheesesteak pizza. </p>

