           <a class="fancybox" rel="group" href="images/Walking-DB-Lunge.jpg" title="Workout"> <img src="images/Walking-DB-Lunge.jpg" alt="workout" id="firstImg"/></a>
                                                                                
            <p>Warming up is a necessary evil. It's like filing your taxes, it sucks, but unless you want the IRS knocking at your door, you have to do it. A proper warmup doesn’t have to last thirty minutes. I know you’ve seen the guy at the gym who spends almost his entire workout on a foam roller, followed by a bunch of other mobility work. Much of this is wasted energy in my opinion. A good warmup should take no more than 20 minutes.</p>
<p class="lastP"></p>
            
<h2>IMPORTANCE OF WARMING UP</h2>
          
            <p>Properly warming up gets the blood flowing through the whole body, increases muscle and connective tissue elasticity, raises core temperature, and prepares your central nervous system for the lifts that will follow. You will be able to lift more weight and you will greatly reduce your risk of injury if you employ a proper warmup before you hit the iron.</p>

<p>A good warmup also has some intangible, mental benefits. Once you start your warmup you are sending a signal to your body that its “go time!” There are times that I don’t feel like lifting heavy, but once I get in the gym and start my warmup my mind focuses on the upcoming task. It’s like a wake-up call to your brain and CNS.</p> 
            
            
            <p>Your warmup needs to get you ready physically and mentally for the upcoming weight workout. We want to move our joints through a full range of motion in a dynamic manner.</p>
            
            <p>Different days of your split will have different specific warmup exercises, but you should have something you do every lifting day to get your body ready. This is called your general warmup. Below is an example of a solid general warmup. After you finish your general warmup you can get to your specific warmup.</p>
            
            
            
            <p class="lastP"></p>
            
            
          <h2>GENERAL WARMUP</h2>
            <a class="fancybox" rel="group" href="images/Lunge-Leg-Workout.jpg" > <img src="images/Lunge-Leg-Workout.jpg" alt="workout" /></a>
            <p><b>Lunges 3x10 yards</b> – Move slowly and fluidly, focus on getting a good stretch.</p>
            <p><b>Spiderman Lunges 3x10 yards</b> – These are similar to lunges except when you lunge with your right forward you also try to touch your right elbow to the ground on the inside of your foot.</p>
            
<p><b>High Knees 3x10 yards</b> – These are classic warmup movements. Focus on keeping your chest over your feet and lifting your knees as high as possible.</p>  
            
            <p><b>Butt Kicks 3x10 (each leg)</b> – Focus on quick leg movements, getting your foot back as high as you can to touch your butt.</p>  
            <p><b>Power Skips 3x10 yards</b> – Just like regular skipping except you want to focus on reaching maximum height with each skip.</p>  
              <p class="lastP"></p>
            
            
            
            
            
            <h2>SPECIFIC WARMUP</h2>  
         <a class="fancybox" rel="group" href="images/bench-press_12_1.jpg" > <img src="images/bench-press_12_1.jpg" alt="workout" /></a>
            <p>
           Your specific warmup will depend upon what day of your split you are on. For example, bench day is going to be different from squat day. The best way to specifically warmup for your lifting is to lift. That means on bench day it might look something like this:</p>

<p>1. Bar x 10</p>
<p>2. 125 x 8</p>
<p>3. 200 x 6</p>
<p>4. 275 x 3</p>
<p>5. 350 x 1</p>
<p>6. Working Sets </p>

<p>Warming up is crucial to success in the weight room, but there is no need to spend countless hours foam rolling or stretching. Just have a good general warmup you do every day and then a specific warmup for each different lifting day. Properly warming up will improve your lifting and help ensure you don’t get hurt.</p>
        
                    
            
            
