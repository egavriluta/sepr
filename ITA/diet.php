           <a class="fancybox" rel="group" href="images/Bodybuilder-Food_0.jpg" title="Workout"> <img src="images/Bodybuilder-Food_0.jpg" alt="workout" id="firstImg"/></a>
                                                                                
            <p>When it comes to diet and nutrition it can get very confusing, one study says one thing, the other study says someting else, how are we supposed to know what's true and what isn't?</p>

<p>Science doesn't always keep up with the real world when it comes to nutrition. Some would say that eating over 1g of protein per pound of bodyweight is a waste, that the body can't utilize that much protein. Really? Tell that to Ronnie Coleman.</p>

<p>I am going to cover the 3 biggest mistakes I see made when it comes to the nutrition of a lifter training intensely! If you are not training hard in the weight room, these recommendations are not for you.</p>
        <p class="lastP"></p> 
        
        <h2>1. NOT ENOUGH PROTEIN</h2>
            <a class="fancybox" rel="group" href="images/bbq-salmon-recipe.jpg" title="NOT ENOUGH PROTEIN"> <img src="images/bbq-salmon-recipe.jpg" alt="workout" /></a>
            <p>This is a huge one. If you are not getting enough protein you are literally robbing your body of potential size and strength. It is the difference between living in a catabolic Hades or an anabolic Valhalla!</p>

<p>If you're training hard and serious about packing on muscle mass, you need to get at least 1g of protein per pound of bodyweight. Ideally, we want to be in the 1.5g/lb. of bodyweight.</p>  
            <p>Try to get your protein from eggs and lean meats (chicken, fish, lean beef, etc.) Game meat is also a great source of protein. Downing that much protein is a tough task so supplement your eating with protein shakes to reach your protein requirement for the day.</p> 
            
            
            
            <p class="lastP"></p>
            
          <h2>2. EATING BAD CARBS</h2>
            <a class="fancybox" rel="group" href="images/donuts_3.jpg" title="EATING BAD CARBS"> <img src="images/donuts_3.jpg" alt="workout" /></a>
            <p>How many carbs you eat in a day is not as important as what kind of carbs you eat. Carbs should come from sources like vegetables, some fruits, whole grains and legumes.</p>

            
<p>Use the glycemic index to pick which carbs you eat. The glycemic index puts a number to individual foods based upon how fast it raises a person's blood glucose levels. The lower the number, the better the carb.</p>  
            
              <p class="lastP"></p>
            
            
            
            
            
            <h2>3. SKIPPING MEALS</h2>  
            <a class="fancybox" rel="group" href="images/bodybuilder-eating.jpg" title="SKIPPING MEALS"> <img src="images/bodybuilder-eating.jpg" alt="workout" /></a>
            <p>
            You can't skip breakfast and lunch, pig out at dinner and expect to grow in any way except round. You should eat three meals and two snacks a day. I like to prescribe meals like this:</p>

<p><ul>
            <li>Meal 1 &#8212; Breakfast</li>
            <li>Meal 2 &#8212; Protein Shake</li>
            <li>Meal 3 &#8212; Lunch</li>
            <li>Meal 4 &#8212; Protein Shake</li>
            <li>Meal 5 &#8212; Dinner</li>
            <li>Meal 6 &#8212; Protein Shake before bed (optional)</li>
            </ul>
            </p>

<p>Another note on meal timing, most of your carbs should come before and right after your workout. This will give you energy for the upcoming workout and then help to replace glycogen stores after intense bouts of exercise.</p>
        <p>This is by no means all there is to nutrition, but it is a good start. If you follow these simple steps I guarantee you will see gains in your future.</p>
