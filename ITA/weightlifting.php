        <p class="TextTitle">Equipment</p>
        
      
<h2>Official Procedure</h2>
<h2>Barbells</h2>
<a class="fancybox" rel="group" href="images/Gewichtheben_Hantelstange_Detail.jpg" title="Barbells"><img src="images/Gewichtheben_Hantelstange_Detail.jpg" alt="weightlifting" height="274" width="410" class="leftImg"/></a>
<p>Olympic weightlifting uses a steel bar (also known as abarbell) with rotating sleeves on either end, holding rubber-coated discs of different weights. This sleeve rotation is important for the Olympic lifts, particularly the snatch and clean movements, because it drastically reduces the rotational inertia of the plates. Without sleeve rotation, the Olympic lifter faces more challenging lifts and a greater risk of injury. 
<br/>A men's Olympic barbell weighs 20 kg (44 lbs) with a shaft diameter of 28mm and a length of 2200mm, whereas a women's Olympic barbell weighs 15 kg (33 lbs) and has a shaft diameter of 25mm with a length of 2100mm. The distance between the sleeves, however, is the same for the men's and the women's bars at 1310mm. The grip texture of the bar is called the knurling, and is distributed differently between the men's and women's bars: the men's has knurling in the centre but the women's does not. The Olympic barbells used in competition are certified by the IWF.</p>

<h2>Bumper Plates</h2>
<p>The weight plates, typically referred to as "bumper plates" because of their rubber design, weigh between 0.5 kg and 25 kg. The bumper plates are constructed out of rubber to allow the weights to be dropped from various heightsâ€”either after a successful lift or during an unsuccessful one. Olympic bumper plates conform to international standards for colouring. That is, 10 kg is green, 15 kg is yellow, 20 kg is blue, and 25 kg is red.</p>
<h2>Competition Iron Plates</h2>
<p>In addition to the rubber bumpers, smaller competition iron plates can be used to add weight in small increments to the bar. The colour designations for these iron plates are as follows: 1 kg is green, 1.5 kg is yellow, 2 kg is blue, 2.5 kg is red, 5 kg and 0.5 kg are white. It is useful to note the colour assignment of these iron plates is consistent with the heavier bumper plates (i.e. 1 kg and 10 kg are green, 1.5 kg and 15 kg are yellow, etc.).</p>
<h2>Collars</h2>
<a class="fancybox" rel="group" href="images/Korea_London_Jang_Miran_03_(7771959092).jpg" title="Jan Miran(Korea)"> <img src="images/Korea_London_Jang_Miran_03_(7771959092).jpg" alt="Weightlifting" height="395" width="512" class="rightImg"/></a>
<p>Weight plates are secured to the bar using collars on each sleeve that weigh exactly 2.5 kg each.</p>
<h2>Singlet</h2>
<p>Lifters typically wear a one-piece, close-fitting leotard often called asinglet. The wearing of a T-shirt underneath the singlet is optional.</p>
    <h2>Weightlifting Belt</h2>
    <p>A weightlifting belt of 120mm maximum width may also be worn to provide lower back support.</p>
    <h2>Chalk</h2>
    <p>Chalk is regularly used by Olympic lifters, generally prior to each attempt at a lift. Lifters rub their hands with the chalk to promote dryness and prevent the bar moving in their hands. In addition to the hands, chalk can be applied to the neck, usually above the collarbone, which is a key point of contact for the bar during a clean and jerk. </p>
    <h2>Tape</h2>
<p>Olympic lifters frequently use tape to cover the areas of their bodies exposed to friction while completing Olympic lifts. Tape is most commonly found on the Olympic lifter's thumb. A taped thumb not only lessens the risk of calluses, it reduces the pain associated with the hook grip.
    Olympic lifters also tape their wrists, preventing exaggerated and uncomfortable joint movement during lifts. For particularly heavy overhead lifts, a taped wrist enables the lifter to regulate wrist extension and delimit thetranslation of the radius and ulna distal heads. However, while taped wrists can prevent wrist and forearm injuries in the short-term, excessive use can lead to weakened connective tissue in the area, increasing the risk of pain and injury. </p>
    <h2>Weightlifting Shoes</h2>
<a class="fancybox" rel="group" href="images/adidas-power-perfect-weightlifting-shoe.jpg" title="Weightlifting Shoes"><img src="images/adidas-power-perfect-weightlifting-shoe.jpg" alt="weightlifting" height="274" width="410" class="leftImg"/></a>
    <p>Perhaps the type of shoes worn by Olympic weightlifters is their most distinctive piece of equipment. Weightlifting shoes are typically designed with a raised heel of 0.5" to 1.5" and one or two metatarsal straps that tighten across the instep of the shoe. The raised heel helps the lifter maintain an upright torso while catching the bar and also allows for a deeper squat under the bar. The soles of the shoes are also quite rigid, helping to resist compression while under heavy loads. The shoes are designed for maximum stability while remaining flexible in the "toebox". This allows the lifter to come up on the toes and to catch the weight on the ball of the back foot during the "jerk" movement of the lift. </p>


        
