       <p>
     <b>Olympic weightlifting,</b> also called Olympic-style weightlifting, or weightlifting, is an athletic discipline in the modern Olympic programme in which the athlete attempts a maximum-weight single lift of a barbell loaded with weight plates.
</p>
<p>The two competition lifts in order are the snatch and the clean and jerk. Each weightlifter receives three attempts in each, and the combined total of the highest two successful lifts determines the overall result within a bodyweight category. Bodyweight categories are different for women and men. A lifter who fails to complete at least one successful snatch and one successful clean and jerk also fails to total, and therefore receives an "incomplete" entry for the competition. The clean and press was once a competition lift, but was discontinued due to difficulties in judging proper form.
</p>
        <p>
In comparison with other strength sports, which test limit strength (with or without lifting aids), Olympic weightlifting tests aspects of human ballistic limits (explosive strength); the lifts are therefore executed faster&#8212;and with more mobility and a greater range of motion during their execution &#8212; than other strength movements. Properly executed, the snatch and the clean and jerk are both dynamic and explosive while appearing graceful, especially when viewed from a recording at a slowed speed.
</p>
        <p>
While there are relatively few competitive Olympic weightlifters, the lifts performed in the Olympics, and in particular their component lifts (i.e. squats, deadlifts, cleans), are commonly used by elite athletes in other sports to train for both explosive and functional strength.
