<p>Because the weight is levitating over your chest in open air, you have to stay tight, commanding complete control of the barbell and maximizing muscular tension.</p>
        <p>Because of the increased time under tension you will not be able to lift as much weight as you could pausing the barbell on your chest. For powerlifting, we generally keep this movement in the 1-3 rep range, and for hypertrophy in the 5-6 rep range.</p>
            
