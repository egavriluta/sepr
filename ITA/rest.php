            <p>The whole idea of periodization is very simply the manipulation of variables in order to achieve certain goals. The variables we have to work with when making our training program are exercise selection, sets, reps, and rest intervals.</p>

<p>The first three variables are pretty well understood by athletes and coaches. Exercise selection, sets and reps are written about ad nauseam and are very important variables to understand.</p>

<p>The last variable, rest intervals, is one that many athletes and coaches either do not understand or do not think is as important as the others. Successful manipulation of rest intervals can mean the difference between achieving your goals or not.</p>
        <p class="lastP"></p> 
        
        <h2>CREATE GOALS</h2>
            
            <p>The first thing you have to do when creating a program is to decide what you want to accomplish. What are your goals? Do you want to be stronger and more powerful? Do you want to increase the size of your muscles? Or are you more concerned with the endurance of your muscles?</p>

<p>Once you have figured out your goal you can begin to adjust variables to suit your needs.</p>  
            <a class="fancybox" rel="group" href="images/20-Minutes-Can-Do-Wonders-Check-Out-How.jpg" title="Workout"><img src="images/Rest/img02.jpg" alt="workout"/></a>
            <h2>REST PERIODS FOR STRENGTH AND POWER</h2>   
            <p>
            Well, you made up your mind that your goal is to become strong and powerful, so what's next? How do we adjust rest periods in order to achieve these goals?</p>

<p>Research shows that when training for strength and power the optimal rest period is between 3 and 5 minutes. This allows the body to restore phosphagen levels in the muscle. Which leads to more powerful contractions on subsequent sets and reps.</p>

<p>In the short term, resting 3-5 minutes with loads in the 50-90% or 1RM has been shown to allow more reps over multiple sets when compared to 1 min rest intervals. Because of the increase in intensity and volume possible, the chronic (or long-term) adaptations to this rest interval were greater increases in absolute strength and power.</p>
            <p class="lastP"></p> 
            <h2>REST INTERVALS FOR HYPERTROPHIC TRAINING</h2>
            <p>
                If your goal is to increase muscle size rather than strength, rest intervals are going to be much shorter.</p>

<p>When training for increased size, multiple sets at a moderate intensity (8-12 RM) with 60-90 second rest intervals has been shown to be more effective due in part to increased levels of growth hormone following exercise performed in this manner.</p>

<p>The idea is that you do not want to be fully recovered from the previous set but you want to be recovered enough to perform the necessary volume. When rest periods are really short (around 30 seconds) the level of intensity and volume is cut way back, which is why for hypertrophic training you want to utilize moderate rest periods of 60-90 seconds.
            </p>
            
           <a class="fancybox" rel="group" href="images/side_plank_on_swiss_ball_ehss1.jpeg" title="Workout"> <img src="images/Rest/img03.jpg" alt="workout"/></a>
          <h2>REST INTERVALS FOR MUSCULAR ENDURANCE</h2>
            
            <p>If your goal is muscular endurance your rest periods are going to be very short. You want to train your body to do a task over and over without getting tired, so the rest periods for muscle endurance purposes are going to be 30-60 seconds.</p>

<p>When training for muscular endurance using short rest periods (with high volume and low intensity) a greater amount of lactic acid builds up in the muscle. This forces the body to increase its lactic acid buffering ability. The body's increased buffering capacity leads to increases in muscular endurance, or the time it takes to get fatigued.</p>

<p>No matter what your goal is you will achieve it quicker if you pay attention to your rest periods and make sure they are in line with your objectives.</p>
            
