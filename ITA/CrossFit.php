        <h2>Can I do it at home?</h2>
        <div>
        <a class="fancybox" rel="group" href="images/CrossFit04.png" title="Cross Fit"><img src="images/CrossFit04.png" alt="CrossFit"/></a>
        </div>
        <p><b>Every day, CrossFit.com puts out the a workout of the day (or WOD), which can be done at home, in a commercial gym, or in a CrossFit gym.</b> </p>
        
        
        <p>Every CrossFit gym will put out their own WOD as well, which can be different from the CrossFit.com site &mdash; if you happen to find a local CrossFit site that you enjoy but don't attend fulltime, it's more than okay to follow their workouts.</p>
        
        <p>The best news about this is the workouts are posted free of charge to anybody that is interested in doing them.  There is even a site dedicated to scaling the workout posted on CrossFit.com to account for different abilities. CrossFits are often prohibitively expensive, so if you love CrossFit but are looking to save money, you can follow along at home or in your office gym provided they have the right equipment.</p>
        
        <p>Many times, you'll run into situations where you can't complete a particular workout because you don't have the right equipment &mdash; do the best you can with what's available to you, and keep track of how you made your modification for tracking purposes.</p>
        <p><b>Now, there are a few issues with following CrossFit at home or by yourself in a gym:</b></p>
        
        <ul>
        <li><b>Nobody is checking your form &mdash;</b> CrossFit requires many incredibly specific movements, if you start by yourself at home, you'll never know if you're doing them wrong and could severely hurt yourself as you increase the amount of weights with which you work.</li>
        <li><b>You probably don't have all of the equipment &mdash;</b> If you're working out at home, you probably don't have a full squat rack,  bumper plates, kettlebells, medicine balls, and so on...so you'll often be creating your own workouts that are modified versions of the online versions.</li>
            <li><b>Nobody is cheering you on &mdash;</b> A HUGE part of CrossFit is the supportive community aspect that comes with each gym.  I guarantee you'd finish a workout a few seconds (or minutes) faster if you had 50 people screaming your name and cheering you toward the finish line.</li>
            <li><b>You will want to buy all of the equipment &mdash;</b> The more you do it, the more you'll want to do it properly. This might not cost as much as an actual box, but it will cost.</li>
        
        </ul>
        
        
