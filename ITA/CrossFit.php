           <h2>What is CrossFit?</h2>
           <div>
           <a class="fancybox" rel="group" href="images/CrossFit02.jpg" title="Cross Fit"><img src="images/CrossFit02.jpg" alt="CrossFit"/></a>
           </div>
    <p><b>CrossFit is advertised, in four words, as ''the sport of fitness.'' </b></p>
           <p>With constantly varied, high-intensity functional movements, CrossFit is a training philosophy that coaches people of all shapes and sizes to improve their physical well-being and cardiovascular fitness in a hardcore yet accepting and encouraging environment.</p>
          <br/>
          
          <p><b>Here's the definition of CrossFit from the official site:</b></p> 
          <p class="rightP"><em>CrossFit is the principal strength and conditioning program for many police academies and tactical operations teams, military special operations units, champion martial artists, and hundreds of other elite and professional athletes worldwide.</em></p>

<p class="rightP"><em>Our program delivers a fitness that is, by design, broad, general, and inclusive. Our specialty is not specializing. Combat, survival, many sports, and life reward this kind of fitness and, on average, punish the specialist.</em></p>

<p class="rightP"><em>CrossFit contends that a person is as fit as they are proficient in each of ten general physical skills: cardiovascular/respiratory endurance, stamina, strength, flexibility, power, speed, agility, balance, coordination, and accuracy</em></p>
           
           <p><b>Or, in nerd speak</b> &mdash; CrossFit is a training program that builds strength and conditioning through extremely varied and challenging workouts.  Each day the workout will test a different part of your functional strength or conditioning, not specializing in one particular thing, but rather with the goal of building a body that's capable of practically anything and everything.</p>

    <p>CrossFit is extremely different from a commercial gym...and not just because you won't find any ellipticals, weight machines (gross), or Zumba classes.  I'll explain what makes CrossFit different later in the article.</p>
           
           <p class="lastP"></p> 
           <h2>Try it!</h2>
           <div>
           <a class="fancybox" rel="group" href="images/CrossFit03.png" title="Cross Fit"><img src="images/CrossFit03.png" alt="CrossFit"/></a>
           </div>
           <p>One of my favorite ''first time'' CrossFit workouts is a benchmark workout named Cindy.  It's a simple bodyweight circuit and can be done practically anywhere &mdash; the only equipment you need is a pullup bar.  It's a favorite for travelling, and shorter versions of it (3 rounds) is often used as a warmup.</p>
           
           <h4>Cindy is:</h4>
           <ul>
           <li>20 minute AMRAP (as many rounds as possible)</li>
           <li>5 pullups</li>
           <li>10 pushups</li>
           <li>15 squats</li>
           
           </ul>
           
           
       <p>What this means is that you put 20 minutes on the clock and then, you do as many rounds as possible (AMRAP) of 5 pullups, 10 pushups, and 15 squats before the time runs out. There is no rest in between rounds &mdash; so as soon as you finish your 15 squats you start on the pullups again.</p>    
           <p>Now, lets look at each movement and how to scale it down if necessary.</p>
           <p>5 Pullups &mdash; You're allowed to kip these (which is a useful skill any time that your goal is not pure strength). If you can't do regular pullups, you can do banded pullups, chair assisted pullups, or jumping pullups instead.  Don't have a pullup bar?  Do bodyweight rows.</p>
           
           <p>10 pushups &mdash; The standard CrossFit pushup is chest to the deck, but if you can't do that, you can substitute knee pushups or wall pushups.</p>
        <p><b>There are also other variations of this workout for beginner athletes.  Some examples are:</b></p>
           
