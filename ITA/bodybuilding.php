<p>In the week leading up to a contest, bodybuilders may decrease their consumption of water, sodium and carbohydrates, the former two to alter how water is retained by the body and the latter to reduce glycogen in the muscle. The day before the show, water is removed from the diet, and diuretics may be introduced, while carbohydrate loading to increase the size of the muscles through replenishment of their glycogen. The goal is to maximize leanness and increase the visibility of veins, or "vascularity." The appearance of veins is further enhanced immediately before appearing on stage by darkening the skin through tanning products, and applying oils to the skin to increase shine. Some competitors will eat sugar-rich foods to increase the visibility of their veins. A final step is the use of weights to fill the muscles with blood and further increase their size.</p>
