         <h2> Cutting and bulking</h2>
         <a class="fancybox" rel="group" href="images/bodybuilding.jpg" title="Cutting and bulking"><img src= "images/bodybuilding.jpg" alt="bodybuilding" height="274" width="350" class="rightImg"/></a>
<p>The general strategy adopted by most present-day competitive bodybuilders is to make muscle gains for most of the year (known as the "off-season") and approximately 12-14 weeks from competition attempt to lose body fat (referred to as "cutting"). The bulking phase entails remaining in a net positive energy balance (calorie surplus). The amount of a surplus that a person remains in is based on the person's goals, as a bigger surplus and longer bulking phase will create more fat tissue. The surplus of calories relative to one's energy balance will ensure that muscles remain in a state of anabolism. The cutting phase entails remaining in a net negative energy balance (calorie deficit). The main goal of cutting is to oxidize fat but also to preserve as much muscle as possible. The larger the calorie deficit, the faster one will lose weight. However, a large calorie deficit will also create the risk of losing muscle tissue.</p>

<p>The precise effectiveness of the cutting and bulking strategy is unknown, with only limited observational case studies on the subject. No studies involving precise hypercaloric feeding combined with resistance exercise have been conducted.</p>

        <h2>     Clean bulking</h2>
        <a class="fancybox" rel="group" href="images/Bodybuilding-Wallpapers-.jpg" title="Competition"><img src="images/Bodybuilding-Wallpapers-.jpg" alt="IFBB" height="300" width="370" class="leftImg" id="firstleftimg"/></a>
<p>Many non-competitive bodybuilders choose not to adopt the conventional strategy, as it often results in significant unwanted fat gain during the "bulking" phase. The attempt to increase muscle mass in one's body without any gain in fat is called clean bulking. Competitive bodybuilders focus their efforts to achieve a peak appearance during a brief "competition season".</p>

        <h2>Dirty bulking</h2>
<p>"Dirty bulking" is the process of eating at a caloric surplus, without finding the exact number macronutrients (carbs, fats, and proteins). Weight lifters who are attempting to gain mass quickly often choose to use the "dirty bulk" method.</p>

